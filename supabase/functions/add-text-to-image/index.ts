import { serve } from "https://deno.land/std@0.190.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.39.3";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const authHeader = req.headers.get("Authorization");
    if (!authHeader) {
      throw new Error("Unauthorized");
    }

    const supabase = createClient(
      Deno.env.get("SUPABASE_URL") ?? "",
      Deno.env.get("SUPABASE_ANON_KEY") ?? "",
      {
        global: {
          headers: { Authorization: authHeader },
        },
      }
    );

    const { imageUrl, textInstruction, userId } = await req.json();

    if (!imageUrl || !textInstruction || !userId) {
      throw new Error("Missing required fields: imageUrl, textInstruction, userId");
    }

    console.log("Adding text to image:", {
      hasImageUrl: !!imageUrl,
      instruction: textInstruction.substring(0, 50),
      userId
    });

    // Get Lovable API key
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY not configured");
    }

    // Call Nano Banana (Gemini 2.5 Flash Image Preview) to edit the image
    console.log("Calling Lovable AI (Nano Banana) for image editing...");
    
    const aiResponse = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        "Authorization": `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash-image-preview",
        messages: [
          {
            role: "user",
            content: [
              {
                type: "text",
                text: textInstruction
              },
              {
                type: "image_url",
                image_url: {
                  url: imageUrl
                }
              }
            ]
          }
        ],
        modalities: ["image", "text"]
      })
    });

    if (!aiResponse.ok) {
      const errorText = await aiResponse.text();
      console.error("Nano Banana API error:", {
        status: aiResponse.status,
        statusText: aiResponse.statusText,
        errorText: errorText.substring(0, 500)
      });
      
      // Handle specific error cases
      if (aiResponse.status === 429) {
        throw new Error("Rate limit exceeded. Please wait a moment and try again.");
      }
      if (aiResponse.status === 402) {
        throw new Error("AI credits depleted. Please add credits to your workspace in Settings.");
      }
      if (aiResponse.status === 401) {
        throw new Error("API key invalid or expired. Please contact support.");
      }
      
      throw new Error(`AI generation failed (${aiResponse.status}): ${errorText.substring(0, 200)}`);
    }

    const aiData = await aiResponse.json();
    console.log("Nano Banana response received");

    // Extract the generated image
    const generatedImageUrl = aiData.choices?.[0]?.message?.images?.[0]?.image_url?.url;
    
    if (!generatedImageUrl) {
      console.error("No image in AI response:", JSON.stringify(aiData, null, 2));
      throw new Error("No image generated by AI");
    }

    console.log("Image with text generated successfully");

    return new Response(
      JSON.stringify({ 
        imageUrl: generatedImageUrl,
        success: true 
      }),
      {
        headers: { ...corsHeaders, "Content-Type": "application/json" },
        status: 200,
      }
    );
  } catch (error: any) {
    console.error("[add-text-to-image] Error:", error);
    const errorMessage = error instanceof Error ? error.message : 'Unknown error occurred';
    return new Response(
      JSON.stringify({ error: errorMessage }),
      {
        status: error instanceof Error && errorMessage.includes('Rate limit') ? 429 :
                error instanceof Error && errorMessage.includes('credits') ? 402 : 400,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
