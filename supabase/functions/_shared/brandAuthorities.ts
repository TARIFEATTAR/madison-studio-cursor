/**
 * Get brand authority profile by name
 * @param authorityName - Name of the authority (e.g., 'wheeler', 'neumeier', 'clow')
 * @returns Brand authority profile markdown content or empty string
 */
export function getBrandAuthority(authorityName: string): string {
  const normalized = authorityName.toLowerCase().trim();
  return BRAND_AUTHORITIES[normalized] || '';
}

/**
 * Get all available brand authority profiles
 * @returns Object mapping authority names to their profiles
 */
export function getAllBrandAuthorities(): Record<string, string> {
  return BRAND_AUTHORITIES;
}

/**
 * Build a formatted section with all brand authority profiles for inclusion in prompts
 * @returns Formatted string with all brand authority profiles
 */
export function buildBrandAuthoritiesSection(): string {
  const parts: string[] = [];
  
  parts.push('\n╔══════════════════════════════════════════════════════════════════╗');
  parts.push('║        BRAND INTELLIGENCE AUTHORITIES                            ║');
  parts.push('║    (Reference these for visual identity & brand strategy)         ║');
  parts.push('╚══════════════════════════════════════════════════════════════════╝');
  parts.push('');
  parts.push('These authorities provide brand intelligence expertise for visual identity,');
  parts.push('brand positioning, and brand campaigns. Use these when analyzing brands,');
  parts.push('developing brand guidelines, or creating brand strategies.');
  parts.push('');
  
  const authorityOrder = ['wheeler', 'neumeier', 'clow'];
  
  authorityOrder.forEach((authority, index) => {
    const profile = BRAND_AUTHORITIES[authority];
    if (profile) {
      parts.push(`━━━ ${index + 1}. ${profile.split('\n')[0].replace('# ', '').replace(' — ', ' — ')} ━━━`);
      parts.push(profile);
      parts.push('');
      parts.push('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
      parts.push('');
    }
  });
  
  parts.push('⚠️ CRITICAL: When referencing these authorities, extract their FRAMEWORKS and TECHNIQUES,');
  parts.push('not literal examples. Always apply brand intelligence principles to the');
  parts.push('user\'s actual brand context and needs.');
  
  return parts.join('\n');
}

